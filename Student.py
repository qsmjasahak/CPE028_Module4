class Student:
    def __init__ (self,name, age):
        self.name = name
        self.age = age
    def __str__ (str):
        return f"{self.name}{(self.age)}"

p1 = Student("Steve Michael Joshua Sahak", 22)
p2 = Student("Walter White", 52)
print("Name: ", p1.name)
print("Age: ", p1.age)
print("Name: ", p2.name)
print("Age: ", p2.age)